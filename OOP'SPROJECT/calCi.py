class Calculater:

    def sum(self,a,b):
        c=a+b
        print(c)

    def substraction(self,a,b):
        c=a-b
        print(c)


    def multiplication(self,a,b):
        c=a*b
        print(c)


    def division(self,a,b):
        c=a/b
        print(c)

object=Calculater()
object.sum(20,30)
object.substraction(50,40)
object.multiplication(34,45)
object.division(50,5)


